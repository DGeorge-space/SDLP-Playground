#include "Colors.h"
int Colors::getRed() {
	return  Red;
}
int Colors::getGreen() {
	return Green;
}
int Colors::getBlue() {
	return  Blue;
}
void Colors::setRed(int red) {
	Red = red;
}
void Colors::setGreen(int green) {
	Green = green;
}
void Colors::setBlue(int blue) {
	Blue = blue;
}
void Colors::setRGB(int red, int green, int blue) {
	Red = red;
	Green = green;
	Blue = blue;
}